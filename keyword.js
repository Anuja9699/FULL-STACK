// let keyword we can reassign but we can not redeclare ,we can redeclare in block only
let a=20;
console.log(a);
a=30;
console.log(a);
{
    let a=50;              
    console.log(a);
}



// // const keyword we can not reassign and redeclare
// in block we can redeclare


const b=30;
console.log(b);
{
    const b=20;
    console.log(b);
    
}

  


